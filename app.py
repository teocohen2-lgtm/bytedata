def process_customer_renewals():

        try:

            print("CHECKING CUSTOMER RENEWALS...")

            spreadsheet = gc.open_by_key(
                "1CeJ8hxjkKly6Ef5hW1spb5E37F7ANKPp-Bsud5x8hpM"
            )

            onboarding_sheet = spreadsheet.worksheet("Onboarding")

            followup_sheet = spreadsheet.worksheet("Payment_Follow_Up")

            records = onboarding_sheet.get_all_records()

            headers = onboarding_sheet.row_values(1)

            status_col = headers.index("status") + 1

            days_remaining_col = headers.index("days_remaining") + 1

            days_overdue_col = headers.index("days_overdue") + 1

            renewal_col = headers.index(
                "renewal_followup_created"
            ) + 1

            today = datetime.now().date()

            for index, row in enumerate(
                records,
                start=2
            ):

                expiry_date = str(
                    row.get(
                        "expiry_date",
                        ""
                    )
                ).strip()

                if not expiry_date:

                    continue

                expiry = datetime.strptime(
                    expiry_date,
                    "%Y-%m-%d"
                ).date()

                days_remaining = (
                    expiry - today
                ).days

                onboarding_sheet.update_cell(
                    index,
                    days_remaining_col,
                    days_remaining
                )

                if days_remaining < 0:

                    onboarding_sheet.update_cell(
                        index,
                        days_overdue_col,
                        abs(days_remaining)
                    )

                    renewal_created = str(
                    row.get(
                        "renewal_followup_created",
                        ""
                    )
                ).strip().lower()

                    if renewal_created != "yes":

                        renewal_lead_id = (
                            "RENEW-" +
                            str(
                                row.get(
                                    "customer_id",
                                    ""
                                )
                            )
                        )

                        followup_sheet.append_row([

                            renewal_lead_id,
                            row.get(
                                "company_name",
                                ""
                            ),
                            row.get(
                                "contact_person",
                                ""
                            ),
                            row.get(
                                "email",
                                ""
                            ),
                            row.get(
                                "phone",
                                ""
                            ),
                            row.get(
                                "subscription_plan",
                                ""
                            ),
                            "RENEWAL",
                            row.get(
                                "payment_link",
                                ""
                            ),
                            "Pending",
                            1,
                            row.get(
                                "assigned_to",
                                "admin"
                            ),
                            datetime.now().strftime(
                                "%Y-%m-%d"
                            ),
                            "Renewal Due",
                            "Auto renewal follow up",
                            datetime.now().strftime(
                                "%Y-%m-%d %H:%M:%S"
                            )

                        ])

                    onboarding_sheet.update_cell(
                        index,
                        renewal_col,
                        "Yes"
                    )

                    # print(
                    #     f"Renewal Created: {renewal_lead_id}"
                    # )

                    

                    onboarding_sheet.update_cell(
                        index,
                        status_col,
                        "Overdue"
                    )

                elif days_remaining <= 7:

                    onboarding_sheet.update_cell(
                        index,
                        days_overdue_col,
                        0
                    )

                    onboarding_sheet.update_cell(
                        index,
                        status_col,
                        "Due Soon"
                    )

                else:

                    onboarding_sheet.update_cell(
                        index,
                        days_overdue_col,
                        0
                    )

                    onboarding_sheet.update_cell(
                        index,
                        status_col,
                        "Active"
                    )

        except Exception as e:

            print(
                "RENEWAL ERROR:",
                e
            )

